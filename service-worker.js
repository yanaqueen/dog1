self.addEventListener('install', event => {
  event.waitUntil(
    caches.open('app-cache').then(cache => {
      return cache.addAll([
        '/',
        '/index.html',
        '/style.css',
        '/app.js',
        '/manifest.json',
        '/icon-192.png',
        '/icon-512.png',
        'J1.jpg',
        'J2.jpg',
        'J3.jpg',
        'J4.jpg',
        'J5.jpg',
        'J7.jpg',
        'Y1.jpg'
      ]);

    })
  );
});

self.addEventListener('fetch', event => {
  event.respondWith(
    caches.match(event.request).then(response => {
      return response || fetch(event.request);
    })
  );
});
